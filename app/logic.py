# ---------- PHOTO SET PICKING ----------
def pick_photo_set(a: Answers) -> int:
    # Priority: occasion ‚Üí eyes ‚Üí undertone ‚Üí skin ‚Üí base
    if a.occasion == "date":
        return 2
    if a.occasion == "party":
        return 3
    if a.occasion == "photo":
        return 4

    if a.eyes == "hooded":
        return 5
    if a.eyes == "small":
        return 6

    if a.undertone == "warm":
        return 7
    if a.undertone == "cool":
        return 8

    if a.skin in ("oily", "combo"):
        return 9
    if a.skin == "dry":
        return 10

    return 1


